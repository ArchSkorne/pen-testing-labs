temperature = int(input("Enter the temperature: "))
if temperature >= 68: 
    print("It's a hot day")
    print("Drink plenty of water")
elif temperature >= 68:
    print("It's a nice day")
    print("Enjoy your day")
elif temperature <= 68:
    print("It's a cool day")
    print("Bundle up")
