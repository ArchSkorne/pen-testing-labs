course = "    Python Programming"
print(course.upper()) # convert to uppercase
print(course.lower()) # convert to lowercase
print(course.title()) # convert to title case
print(course.strip()) # remove whitespace
print(course.lstrip()) # remove leading whitespace
print(course.rstrip()) # remove trailing whitespace
print(course.find("Pro")) # find the index of substring "Pro"
print(course.replace("P", "K")) # replace substring
print(course.replace("Python", "Java")) # replace substring
print(course.replace("Programming", "Programming with Python")) # replace substring
print("Python" in course) # check if substring "Python" is in course
print("Programming" in course) # check if substring "Programming" is in course
