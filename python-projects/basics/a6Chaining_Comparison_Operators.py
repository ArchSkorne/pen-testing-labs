# Chaining Comparison Operators
# You can chain comparison operators in Python to make your code more concise and readable.
# For example, instead of writing:
age = 25
if age >= 18 and age <= 65:
    print("Eligible for work")
# You can chain the comparisons like this:
if 18 <= age <= 65:
    print("Eligible for work")
# This checks if age is between 18 and 65, inclusive.