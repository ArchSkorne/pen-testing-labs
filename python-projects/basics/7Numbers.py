# Numbers in Python
print(10+3) # addition
print(10-3) # subtraction
print(10*3) # multiplication
print(10/3) # division
print(10//3) # floor division
print(10%3) # modulus
print(10**3) # exponentiation

x = 10
x += 3 # x = x + 3
print(x)
x -= 3 # x = x - 3
print(x)
x *= 3 # x = x * 3
print(x)
x /= 3 # x = x / 3
print(x)
x //= 3 # x = x // 3
print(x)
x %= 3 # x = x % 3
print(x)
x **= 3 # x = x ** 3
print(x)
