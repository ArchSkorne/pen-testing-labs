course = "Python Basics"
print(len(course))  # length of the string
print(course[0])  # first character
print(course[1])  # second character
print(course[-1])  # last character
print(course[0:6])  # substring from index 0 to 5
print(course[0:3])  # substring from index 0 to 3
print(course[7:])  # substring from index 7 to end
print(course.upper())  # convert to uppercase
print(course.lower())  # convert to lowercase
print(course.find("B"))  # find the index of substring "B"
print(course.replace("Basics", "Advanced"))  # replace substring
print("Python" in course)  # check if substring "Python" is in course
